const uint32_t GB2312_NUM = (sizeof(gb2312_16_16_Arr)) / (sizeof(GB2312_16_16FontTypeDef));

/**
  *@brief 获取GB2312 16*16的字模。
  *@param  codeH 字模编码高
  *@param  codeH 字模编码低
  *@param  pBuff 字模buff
  *@retval 0
  */
uint8_t GetFontGb2312_16_16(uint8_t codeH, uint8_t codeL, uint8_t *pBuff)
{
    uint32_t i, offset, code;

    code = (codeL << 8) | codeH;

    for (offset = 0; offset < GB2312_NUM; offset++)
    {
        if (code == gb2312_16_16_Arr[offset].code.fullCode)
        {
            break;
        }
    }

    if (offset > GB2312_NUM)
    {
        for (i = 0; i < GB2312_EACH_SIZE; i++)
        {
            pBuff[i] = 0;
        }
    }
    else
    {
        for (i = 0; i < GB2312_EACH_SIZE; i++)
        {
            pBuff[i] = gb2312_16_16_Arr[offset].data[i];
        }
    }

    return 0;
}

